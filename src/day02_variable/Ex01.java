package day02_variable;

public class Ex01 {
	public static void main(String[] args) {
		/*
		 변수 작성 방법
		 - 자료형(type) 변수 이름;
		 - 자료형 변수 이름 = 값;
		 변수
		 - 무언가(date)를 저장하기 위한 공간
		 변수명 작성 규칙
		 - 될 수 있으면 영어로 작성한다
		 - 변수명의 첫 글자는 소문자로 작성한다
		 - 만약 합성어가 존재한다면 합성어의 첫 글자는 대문자로 짓는다
		 */
		/*
		 자료형
		 - 정수
		 1) short : 2byte
		 2) int : 4byte
		 - 실수
		 1) float : 4byte
		 2) double : 8byte
		 - 문자
		 1) byte : 1byte(음수와 양수 표현 가능)
		 2) char : 2byte(양수만 표현 가능)
		 - 논리형
		 1) boolean : 1byte(true(참), false(거짓) 만 표현 가능)
		 컴퓨터 단위
		 - bit, byte, kbyte, mbyte, gbyte,
		 */
	}

}
